package basics;

public class PrimitiveDataTypesDemo {

	public static void main(String[] args) {
		byte byteMax=127;
		byte byteMin=-128;
		System.out.println("Min range of byte is"+byteMin+"Max range of byte is"+byteMax);
		
		short shortMax=32767;
		short shortMin=-32768;
		System.out.println("Minshort range of byte is"+shortMin+"Maximum range of byte is"+shortMax);
		
		int maxint=123654789;
		int minint=789654123;
		System.out.println("Minshort range of byte is"+minint+"Maxshort range of byte is"+maxint);
		
		long maxLong=1234567891234567891L;
		long minLong=-789456123789612378L;
		System.out.println("Minlong range of byte is"+minLong+"Maxlong range of byte is"+maxLong);
		
		float f=3214.1414555548552f;
		double d=3214.141458855888f;
		System.out.println("float value is"+f+"double value is"+d);
		boolean flag=false;
		System.out.println("boolean value is"+flag);
		
		
	
	}

}
